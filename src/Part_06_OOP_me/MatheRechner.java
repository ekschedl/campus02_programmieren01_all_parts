public class MatheRechner {


    public static int addieren(int a, int b) {
        return a + b;
    }

    public static int substrahieren(int a, int b) {
        return a - b;
    }

    public static double multiplizieren(int a, int b) {
        return a * b;
    }

    public static  double dividieren(int a, int b) {
        return (double) a / b;
    }


}
